    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        Connection conc;
        PreparedStatement pre;
        try{
            try {
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(IJFBooking.class.getName()).log(Level.SEVERE, null, ex);
            }
            conc = (Connection)DriverManager.getConnection("jdbc:mysql://localhost/air_line","root","munchy");
            String sql = "SELECT Customer_ID,NIC_ID,First_Name,Last_Name,Contact_No,Gender FROM customer WHERE Customer_ID=?";
            pre =conc.prepareStatement(sql);
            pre.setString(1,FldCustomerID.getText());

            ResultSet rs = pre.executeQuery();
            while(rs.next()) { 
            FldCustomerID.setText(rs.getString("Customer_ID"));
            FldUserID.setText(rs.getString("NIC_ID"));
            FldF_Name.setText(rs.getString("First_Name"));
            FldL_Name.setText(rs.getString("Last_Name"));
            FldContact.setText(rs.getString("Contact_No"));
            FldGender.setText(rs.getString("Gender"));
            }
        }
        catch(SQLException ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed


