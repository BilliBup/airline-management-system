    private void btnGoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoActionPerformed
       try{
          Connection conc;
          PreparedStatement pre = null;
          int cc;
          String arrival = CBArrival.getSelectedItem().toString();
          String departure = CBDeparture.getSelectedItem().toString();
          
          try {
              Class.forName("com.mysql.jdbc.Driver");
          } catch (ClassNotFoundException ex) {
              Logger.getLogger(IJFBooking.class.getName()).log(Level.SEVERE, null, ex);
          }
          conc= (Connection) DriverManager.getConnection("jdbc:mysql://localhost/air_line","root","munchy");
          java.sql.Statement s = conc.createStatement();
          pre =conc.prepareStatement("select * from flight where Arrival=? && Departure=?");
          pre.setString(1,arrival);
          pre.setString(2,departure);
          ResultSet rs;
          rs = pre.executeQuery();
          ResultSetMetaData RSMD = rs.getMetaData();
           cc = RSMD.getColumnCount();
           DefaultTableModel DFT = (DefaultTableModel)TableFlight.getModel();
           DFT.setRowCount(0);
           while(rs.next()){
               Vector v2 = new Vector();
               for(int i=1; i <= cc; i++){
                   v2.add(rs.getString("Flight_ID"));
                   v2.add(rs.getString("Flight_Name"));
                   v2.add(rs.getString("Arrival"));
                   v2.add(rs.getString("Departure"));
                   v2.add(rs.getString("Date"));
               }
               DFT.addRow(v2);
           }
        }
        catch(SQLException ex){  
            System.out.println(ex);
           Logger.getLogger(IJFBooking.class.getName(), title).log(Level.SEVERE,null,ex); 
        }
        
    }//GEN-LAST:event_btnGoActionPerformed

