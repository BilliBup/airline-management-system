    public void autoId() throws ClassNotFoundException, SQLException{
        try{
            try{
            Class.forName("com.mysql.jdbc.Driver");   
           }
           catch(ClassNotFoundException ex){
            Logger.getLogger(IJFAddFlight.class.getName(), title).log(Level.SEVERE,null,ex);
           }
        
            conc = DriverManager.getConnection("jdbc:mysql://localhost/air_line","root","munchy");
            java.sql.Statement s = conc.createStatement();
            ResultSet rs = s.executeQuery("Select MAX(Flight_ID) from flight");
            rs.next();
            if(rs.getString("MAX(Flight_ID)")==null){
                FldFlightID.setText("FL001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(Flight_ID)").substring(2,rs.getString("MAX(Flight_ID)").length()));
                id++;
                FldFlightID.setText("FL"+String.format("%3d", id).replace(' ', '0'));
            }
        }
        catch(SQLException ex){
            
           Logger.getLogger(AddCustomer.class.getName(), title).log(Level.SEVERE,ex.getMessage(),ex); 
        }
        
            
    }
    
    
