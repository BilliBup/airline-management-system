    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = FldName.getText();
        String arrival = FldArrival.getText();
        String seat = FldSeats.getText();
        String departure = FldDeparture.getText();
        String Fair = FldFair.getText();
        String f_ID = FldFlightID.getText();
        String ter = FldTerminal.getText();
        String time = FldTime.getText();
        DateFormat dt = new SimpleDateFormat("yyyy-mm-dd");
        String d = dt.format(DateChoDate.getDate());
        
        
        try{
            try{
            Class.forName("com.mysql.jdbc.Driver");   
           }
           catch(ClassNotFoundException ex){
            Logger.getLogger(IJFAddFlight.class.getName(), title).log(Level.SEVERE,null,ex);
           }
        
            conc = DriverManager.getConnection("jdbc:mysql://localhost/air_line","root","munchy");
            
            pre = conc.prepareStatement("insert into flight(Flight_ID,Flight_Name,Arrival,Departure,Seats,Fare,Duration,Date,Terminal )values(?,?,?,?,?,?,?,?,?)");
                        pre.setString(1,f_ID   );
                        pre.setString(2, name);
                        pre.setString(3, arrival);
                        pre.setString(4, departure);
                        pre.setString(5, seat);
                        pre.setString(6, Fair);
                        pre.setString(7, time);
                        pre.setString(8, d);
                        pre.setString(9, ter);
                        
                        pre.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Flight Added");
                   
        }
        catch(SQLException ex){
            System.out.println(ex);
           //Logger.getLogger(IJFAddFlight.class.getName(), title).log(Level.SEVERE,null,ex); 
        }
        
        
    }//GEN-LAST:event_btnAddActionPerformed

